package com.zykie.bean;

public class Actor {
	private String name;
	private String classnmae;
	private String classpath;
	private String type_language;
	private String description;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getClassnmae() {
		return classnmae;
	}
	public void setClassnmae(String classnmae) {
		this.classnmae = classnmae;
	}
	public String getClasspath() {
		return classpath;
	}
	public void setClasspath(String classpath) {
		this.classpath = classpath;
	}
	public String getType_language() {
		return type_language;
	}
	public void setType_language(String typeLanguage) {
		type_language = typeLanguage;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	
}
