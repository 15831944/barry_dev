package bean;

public class Account {
	private String zky_class;
	private String zky_name;
	private String zky_uname;
	public String getZky_class() {
		return zky_class;
	}
	public void setZky_class(String zkyClass) {
		zky_class = zkyClass;
	}
	public String getZky_name() {
		return zky_name;
	}
	public void setZky_name(String zkyName) {
		zky_name = zkyName;
	}
	public String getZky_uname() {
		return zky_uname;
	}
	public void setZky_uname(String zkyUname) {
		zky_uname = zkyUname;
	}
}
